    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        if (txt_nama.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama belum diisi!");
            txt_nama.requestFocus();
        } else if (txt_noHp.getText().trim().equals("")) {
            txt_noHp.requestFocus();
        } else if (txt_email.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Email belum diisi");
            txt_email.requestFocus();
        } else {
            String nama_pegawai = "", no_hp = "", email = "";
            nama_pegawai = txt_nama.getText();
            String kode = txt_kode.getText();
            no_hp = txt_noHp.getText();
            email = txt_email.getText();
            try {
                connect = new koneksi().getKoneksi();
                String query
                        = "INSERT INTO petugas (kode, nama_pegawai, no_hp, email) VALUES(?,?,?,?)";

                PreparedStatement psmt = connect.prepareStatement(query);
                psmt.setString(1, kode);
                psmt.setString(2, nama_pegawai);
                psmt.setString(3, no_hp);
                psmt.setString(4, email);

                psmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Menyimpan Data Berhasil", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                tampilkan();

            } catch (Exception e) {
                System.out.println("Menyimpan Data Gagal " + e);
            }
        }


    }//GEN-LAST:event_btn_simpanActionPerformed
            
            
            
            
            
    

