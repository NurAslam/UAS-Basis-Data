    public void tampilkan() {
        tab = (DefaultTableModel) tbForm.getModel();
        tab.setRowCount(0);
        try {
            konek.st = konek.conn.createStatement();
            String query = "SELECT id_pegawai,kode, nama_pegawai, no_hp, email FROM petugas";
            konek.rs = konek.st.executeQuery(query);
            while (konek.rs.next()) {
                String ID = konek.rs.getString("id_pegawai");
//                String Kode = konek.rs.getString("kode");
                String nama = konek.rs.getString("nama_pegawai");
                String noHp = konek.rs.getString("no_hp");
                String email = konek.rs.getString("email");

                String[] data = {ID, nama, noHp, email};
                tab.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("Gagal Ambil Data" + e);
        }
        tbForm.setModel(tab);
    }

