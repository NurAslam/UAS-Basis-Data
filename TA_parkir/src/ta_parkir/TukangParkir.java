public void tampil_keluar(){
        DecimalFormat rupiah = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();
        formatRp.setCurrencySymbol("Rp ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');
        rupiah.setDecimalFormatSymbols(formatRp);
        String query = "SELECT No_tiket, nama_pegawai, nomor_polisi, jenis, tgl_keluar, jam_keluar, biaya FROM parkir_keluar, petugas WHERE parkir_keluar.id_pegawai = petugas.id_pegawai and biaya <> 0 ORDER BY tgl_keluar DESC,jam_keluar DESC LIMIT 15";
        tabmode = (DefaultTableModel) tbKeluar.getModel();
        try {
            st = konek.getKoneksi().createStatement();
            
            rs = st.executeQuery(query);
            while (rs.next()) {
                String no_tiket = rs.getString("No_tiket");
                String pegawai = rs.getString("nama_pegawai");
                String no_pol = rs.getString("nomor_polisi");
                String jenis = rs.getString("jenis");
                String tgl_keluar = rs.getString("tgl_keluar");
                String jam_keluar = rs.getString("jam_keluar");
                String biaya = rs.getString("biaya");
                Double biaya2 = Double.parseDouble(biaya);
                String biaya_final = (rupiah.format(biaya2));
                
                String [] data = {no_tiket,pegawai, no_pol, jenis, tgl_keluar, jam_keluar, biaya_final};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        tbKeluar.setModel(tabmode);
    }
