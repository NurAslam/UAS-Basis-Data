    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        if(txtKNoTiket.getText().isEmpty())
        JOptionPane.showMessageDialog(this, "No Tiket masih kosong!");
        else {
            DecimalFormat rupiah = (DecimalFormat) DecimalFormat.getCurrencyInstance();
            DecimalFormatSymbols formatRp = new DecimalFormatSymbols();
            formatRp.setCurrencySymbol("Rp ");
            formatRp.setMonetaryDecimalSeparator(',');
            formatRp.setGroupingSeparator('.');
            rupiah.setDecimalFormatSymbols(formatRp);

            String sql = "SELECT *, DATEDIFF(CURDATE(), tgl_masuk) durasi_hari, (TIME_FORMAT(CURTIME(),'%r')-TIME_FORMAT(jam_masuk, '%r')) durasi_jam, nama_pegawai "
            + "FROM parkir_keluar, petugas WHERE  tb_parkir.id_pegawai = petugas.id_pegawai and No_tiket='"+txtKNoTiket.getText()+"' AND biaya=0";
            try
            {
                st = konek.getKoneksi().createStatement();
                rs = st.executeQuery(sql);
            } catch (SQLException ex){
                JOptionPane.showMessageDialog(this, "Kesalahan query"+ex);
            }
            int size=0;
            try {
                while (rs.next()){
                    size++;
                    txtNoPol.setText(rs.getString("nomor_polisi").toString());
                    txtKJenis.setText(rs.getString("jenis").toString());
                    txtKTglMasuk.setText(rs.getString("tgl_masuk").toString());
                    txtKJamMasuk.setText(rs.getString("jam_masuk").toString());
                    txt_idPegawai.setText(rs.getString("id_pegawai").toString());
                    int hari = Integer.parseInt(rs.getString("durasi_hari"));
                    int jam = Integer.parseInt(rs.getString("durasi_jam"));
                    int biaya, biaya_akhir;
                    if(txtKJenis.getText().equals("Mobil")){
                        txtKBiaya.setText(rupiah.format(5000));
                        biaya = 5000;
                    } else {
                        txtKBiaya.setText(rupiah.format(2000));
                        biaya = 2000;
                    }
                    if(hari>0){
                        txtKDurasiH.setText(""+hari+" hari");
                        txtKDurasiJ.setText("-");
                        biaya_akhir = biaya + (5000*hari);
                        txtKBiayaAkhir.setText(rupiah.format(biaya_akhir));
                        lblbiaya.setText(""+biaya_akhir);
                    }
                    else{
                        txtKDurasiH.setText("0 hari");
                        txtKDurasiJ.setText(konek.rs.getString("durasi_jam").toString()+" jam");
                        biaya_akhir = biaya + (1000*jam);
                        txtKBiayaAkhir.setText(rupiah.format(biaya_akhir));
                        lblbiaya.setText(""+biaya_akhir);
                    }

                }
                if(size==0)
                {
                    JOptionPane.showMessageDialog(this,"Data tidak ditemukan");
                    return;
                }
                rs.close();
                st.close();
            } catch (SQLException ex){
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

